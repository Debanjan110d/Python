a= "Huha\nGukha"
print(a)
#it will print the string in two lines
a= "Huha\tGukha"
print(a)
#it will create a tab like gap in between two words
a= "Huha\bGukha"
print(a)
#it will remove the last character of the string
a= "Huha\"Gukha\""
print(a)
#it will print the string in double quotes
a= "Huha\'Gukha\'"
print(a)
#it will print the string in single quotes
a= "Huha\\Gukha\\"
print(a)
#it will print the string in backslash

